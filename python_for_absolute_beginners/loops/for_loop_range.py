print("range() with one parameter")
one_input = range(5)

for num in one_input:
    print(num)



print("range() with two parameter")

two_input = range(5, 10)

for num in two_input:
    print(num)



print("range() with three parameter")

three_input = range(1, 20, 3)

for num in three_input:
    print(num)